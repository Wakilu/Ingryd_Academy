

# If Statement
# example:
age = int(input("Enter your age: "))
if age >= 18:
    print("You're an adult.\nYou can vote")


# If-else statement
# example

age = int(input("Enter your age: "))
if age >= 18:
    print("You're an adult.\nYou can vote")
else:
    print("You cannot vote")


